							TATA TERTIB PERPUSTAKAAN<br>
							Setiap pengunjung wajib:<br>
								1. Menaati tata tertib perpustakaan<br>
								2. Mengisi buku kunjungan<br>
								3. Menjaga kebersihan dan keamanan<br>
								4. Menaruh kembali ke tempat semula buku yang telah dibaca<br>
								5. Mengembalikan buku yang telah dipinjam tepat pada waktunya<br>
								6. Dilarang membawa tas ke dalam ruangan perpustakaan
								7. Dilarang membawa makanan/minuman<br>
								8. Dilarang membuat gaduh di dalam ruang perpustakaan<br>
								9. Dilarang masuk/berada di ruang kerja petugas<br>

                          
                            SANKSI<br>
                            1. Keterlambatan; peminjam tidak mengembalikan buku yang dipinjam sesuai dengan tanggal kembali, maka peminjam wajib melaporkan kepada petugas dan tidak diperkenankan meminjam lagi sebelum buku tersebut dikembalikan.<br>
                            2. Buku Hilang; peminjam yang terbuktu menghilangkan buku yang dipinjam harus mengganti dengan buku yang sama; peminjam tidak bisa menggantu dengan buku yang sama, maka peminjam wajib mengembalikan dengan cara memfoto copy buku yang hilang.<br>
                            3. Buku Rusak; peminjam yang terbukti telah merusak buku yang dipinjam baik disengaja maupun tidak disengaja, maka pihak perpustakaan berhak untuk tidak memberikan pinjaman buku sebelum kerusakan buku tersebut diperbaiki.<br>
                            
